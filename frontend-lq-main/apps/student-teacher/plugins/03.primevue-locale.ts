export default defineNuxtPlugin((nuxtApp) => {
  const primeVue = usePrimeVue();

  // Access i18n from nuxtApp instead of using useI18n() directly
  const i18n = nuxtApp.$i18n as { locale: { value: string } };

  // Set initial locale
  const initialLocale = primeVueLocales[i18n.locale.value as keyof typeof primeVueLocales] || primeVueLocales.en;
  primeVue.config.locale = initialLocale;

  // Watch for locale changes and update PrimeVue locale
  watch(
    () => i18n.locale.value,
    (newLocale) => {
      const pvLocale = primeVueLocales[newLocale as keyof typeof primeVueLocales] || primeVueLocales.en;
      primeVue.config.locale = pvLocale;
    },
  );
});
